import Foundation

struct CanadaDataModel: Codable{
    let title: String
    let rows: [Row]
}

struct Row: Codable{
    let title, rowDescription: String?
    let imageHref: String?

    enum CodingKeys: String, CodingKey {
        case title
        case rowDescription = "description"
        case imageHref
    }
}
